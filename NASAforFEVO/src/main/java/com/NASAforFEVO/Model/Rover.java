package com.NASAforFEVO.Model;

public class Rover{
    public int id;
    public String name;
    public String landing_date;
    public String launch_date;
    public String status;
	public Rover() {
			}
	public int getId() {
		return id;
	}
	public String getName() {
		return name;
	}
	public String getLanding_date() {
		return landing_date;
	}
	public String getLaunch_date() {
		return launch_date;
	}
	public String getStatus() {
		return status;
	}
}

